func TestAnalyzeWorkingTreeAgainstBase(t *testing.T) {
	root := t.TempDir()
	runGit(t, root, "init", "-b", "main")
	runGit(t, root, "config", "user.email", "test@example.com")
	runGit(t, root, "config", "user.name", "Test")
	write(t, root, "keep.go", "package sample\nfunc keep(x int) { println(x) }\n")
	write(t, root, "delete.go", "package sample\nfunc dead(x int) { if x > 0 {} }\n")
	write(t, root, "old.go", "package sample\nfunc moved() {}\n")
	runGit(t, root, "add", ".")
	runGit(t, root, "commit", "-m", "base")
	runGit(t, root, "switch", "-c", "feature")

	write(t, root, "keep.go", "package sample\nfunc keep(x int) { if x > 0 { println(x) } }\n")
	if err := os.Remove(filepath.Join(root, "delete.go")); err != nil {
		t.Fatal(err)
	}
	if err := os.Rename(filepath.Join(root, "old.go"), filepath.Join(root, "new-name.go")); err != nil {
		t.Fatal(err)
	}
	runGit(t, root, "add", "old.go", "new-name.go")
	write(t, root, "untracked.go", "package sample\nfunc added(x int) { if x > 0 {} }\n")

	result, err := Analyze(Options{Root: root, Base: "main", ExcludeGenerated: true})
	if err != nil {
		t.Fatal(err)
	}
	if result.ComplexityAdded != 3 || result.ComplexityRemoved != 2 {
		t.Fatalf("complexity +%d -%d, want +3 -2: %+v", result.ComplexityAdded, result.ComplexityRemoved, result.Files)
	}
	if len(result.Files) != 4 {
		t.Fatalf("files = %d, want 4: %+v", len(result.Files), result.Files)
	}
	assertFile(t, result, "keep.go", "modified", 1, 0)
	assertFile(t, result, "delete.go", "deleted", 0, 2)
	assertFile(t, result, "new-name.go", "renamed", 0, 0)
	assertFile(t, result, "untracked.go", "added", 2, 0)
}

